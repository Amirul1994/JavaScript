// ? : conditional operator 
// ( ) ? " ":" " 

// if the condition within ( ) is true, 
// then the value " ": will be stored in the variable 

// if the condition within ( ) is false, 
// then the value :" " will be stored in the variable 

var a = ( 1 < 8 ) ? "Hello":"World";  
console.log (a); 

var b = ( 1 > 8 ) ? "Hello":"World"; 
console.log (b);  

