// || : OR  
// if one is true ,then the result is true
// if both are false ,then the result is false
console.log( true || true ); 
console.log( true || false); 
console.log( false || true );
console.log( false || false ); 

// && : AND 
// if both are true, then the result is true
// if one is false, then the result is false
console.log( true && true ); 
console.log( true && false ); 
console.log( false && false );  

// ! : NOT 
// if the operand is true, the result is false
// if the operand is false, the result is true 
console.log( !true ); 
console.log( !false ); 

console.log ( ( 4 < 6) || ( 1 < 7 ) ); 
console.log ( ( 4 > 6 ) || ( 1 < -9 ) );   

console.log ( ( 4 > 6 ) && ( 1 < -9 ) ); 
console.log ( true || true || false ); 

console.log( ( 4 < 6 ) && !( 1 < -9) ); 