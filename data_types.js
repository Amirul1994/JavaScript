// data types, the following types are commonly 
// used for storing data 

// Numbers : 1, 2.345 (float)

// Strings : collection of characters ( letters, numbers, punctuation etc.)
// Strings are written within ' ' and  " " .

// Booleans: Either true or false. e.g var a = true, var b = false

// Arrays: Collection of items or values in a variable. 
// Written within square brackets []
// e.g  var num = [1, 2, 3];
// var name = ["Karim", "Rahim"]; 
// var values = [1, "Karim", 3, "Rahim"];

// Objects: collection of property:value pairs
// written with curly braces 
// e.g   var man = { name: "Rahim", age: 21, home: "Dhaka" }; 
// Similar to html attributes 

// Some more types of data 
// undefined 
// Empty value 
// null 
// NaN : Not a Number